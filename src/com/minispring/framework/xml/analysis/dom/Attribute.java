package com.minispring.framework.xml.analysis.dom;

/**
 * 
 * @author 	杨亚龙
 * @date	2018-3-5
 * 节点中的属性类
 */
public class Attribute {

	private String name;
	private String value;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
